import { PredictionFeatures } from './feature-builder.js';
import { ConfidenceResult } from './confidence-engine.js';

export interface PredictionExplanation {
  summary: string;
  keyFactors: string[];
  counterFactors: string[];
  dataQualityNote: string;
  modelDisclaimer: string;
}

export class ExplanationBuilder {
  build(
    features: PredictionFeatures,
    confidence: ConfidenceResult,
    favoredTeam?: string,
  ): PredictionExplanation {
    const keyFactors: string[] = [...confidence.factors];
    const counterFactors: string[] = [];

    if (features.homeInjuryImpact > 0.3) counterFactors.push(`Home injury concern (impact: ${(features.homeInjuryImpact * 100).toFixed(0)}%)`);
    if (features.awayInjuryImpact > 0.3) counterFactors.push(`Away injury concern (impact: ${(features.awayInjuryImpact * 100).toFixed(0)}%)`);
    if (features.dataCompleteness < 0.5) counterFactors.push('Limited data available — increase caution');

    const favoredPart = favoredTeam ? `${favoredTeam} is favored ` : '';
    const summary = `${favoredPart}with ${confidence.tier} confidence (${(confidence.score * 100).toFixed(0)}%). ${keyFactors.length > 0 ? `Key drivers: ${keyFactors.join(', ')}.` : 'No strong signals identified.'}`;

    const dataQualityNote =
      features.dataCompleteness >= 0.8
        ? 'High data quality — providers responding with full context.'
        : features.dataCompleteness >= 0.5
          ? 'Moderate data quality — some provider data missing.'
          : 'Low data quality — treat outputs as directional only.';

    const modelDisclaimer =
      'This output is generated by sports-jelly-sdk for informational purposes only. It does not constitute financial or betting advice. Confidence scores are model estimates, not guarantees.';

    return { summary, keyFactors, counterFactors, dataQualityNote, modelDisclaimer };
  }
}
